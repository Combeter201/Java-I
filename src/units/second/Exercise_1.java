package units.second;

public class Exercise_1
{
    public static void main(String[] args)
    {
        int x = 5;
        int y = 7;
        //Before Swap
        System.out.println(x);
        System.out.println(y);

        int z = x;
        x = y;
        y = z;
        //Swap

        System.out.println(x);
        System.out.println(y);
        //After the Swap
    }
}
